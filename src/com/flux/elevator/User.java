package com.flux.elevator;

public class User {

	private int inFloor;
	private int outFloor;
	private long giveUpTime;

	public int getInFloor() {
		return inFloor;
	}

	public void setInFloor(int inFloor) {
		this.inFloor = inFloor;
	}

	public int getOutFloor() {
		return outFloor;
	}

	public void setOutFloor(int outFloor) {
		this.outFloor = outFloor;
	}

	public long getGiveUpTime() {
		return giveUpTime;
	}

	public void setGiveUpTime(long giveUpTime) {
		this.giveUpTime = giveUpTime;
	}
}
